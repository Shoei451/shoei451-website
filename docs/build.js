#!/usr/bin/env node
// docs/build.js
// MDファイルをスキャンして docs/file-tree.js を自動生成するスクリプト
// 使い方: node docs/build.js

import { readdirSync, statSync, writeFileSync } from 'fs';
import { join, relative, dirname, basename } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT   = join(__dirname, '..');
const OUTPUT_FILE = join(__dirname, 'file-tree.js');

// ===== 設定 =====

// スキャン対象外
const SKIP_DIRS = new Set([
  '.git', '.github', '.vscode',
  'node_modules',
  'images', 'css', 'js', 'scripts',
]);

// 親ディレクトリのパス（リポジトリ相対）→ カテゴリ表示名
// キーは repoRelative なパス（例: "docs", "others/healthcare-3-final/units"）
// 未定義のディレクトリは自動でパスをそのままラベルに使う
const DIR_LABELS = {
  'docs':                              '📄 Docs',
  'others/healthcare-3-final/units':   '🏥 保健',
  'koten/joushiki':                    '📜 古典 / 常識カード',
  'koten/jodoushi':                    '📜 古典 / 助動詞',
  'history':                           '📚 History',
  'history/china':                     '📚 History / 中国史',
  'history/worldhistory':              '📚 History / 世界史',
  'geography':                         '🌍 Geography',
  'seikei':                            '🏛 政治・経済',
  'informatics1':                      '💻 情報',
  'home_economics':                    '🏠 家庭科',
  'templates/frashcards':              '📋 Templates / Flashcards',
  'attribution':                       '📝 Attributions',
  'projects':                          '🚀 Projects',
};

// カテゴリの表示順（上記キーの順序で並べる）
const LABEL_ORDER = Object.keys(DIR_LABELS);

// ===== スキャン =====

function collectMdFiles(dir) {
  const results = [];
  for (const entry of readdirSync(dir)) {
    if (SKIP_DIRS.has(entry)) continue;
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) {
      results.push(...collectMdFiles(full));
    } else if (entry.endsWith('.md')) {
      results.push(full);
    }
  }
  return results;
}

const allFiles = collectMdFiles(REPO_ROOT);

// ===== 親ディレクトリ単位でグループ化 =====
// Map<parentDirRepoRel, Array<{name, path}>>
const groups = new Map();

for (const absPath of allFiles) {
  const repoRel  = relative(REPO_ROOT, absPath).replace(/\\/g, '/'); // e.g. "docs/example.md"
  const docsRel  = relative(__dirname, absPath).replace(/\\/g, '/'); // e.g. "example.md" or "../others/..."

  const parts    = repoRel.split('/');
  const parentKey = parts.slice(0, -1).join('/');                    // e.g. "others/healthcare-3-final/units"
  const fileName  = basename(absPath, '.md');                        // 拡張子なしファイル名

  if (!groups.has(parentKey)) groups.set(parentKey, []);
  groups.get(parentKey).push({ name: fileName, path: docsRel });
}

// ===== FILE_TREE 配列を組み立てる =====

// LABEL_ORDER に定義されたキーを優先、残りはアルファベット順で末尾に追加
const knownKeys   = LABEL_ORDER.filter(k => groups.has(k));
const unknownKeys = [...groups.keys()]
  .filter(k => !DIR_LABELS[k])
  .sort();

const fileTree = [...knownKeys, ...unknownKeys].map(key => ({
  label: DIR_LABELS[key] ?? `📁 ${key}`,
  files: groups.get(key),
}));

// ===== 書き出し =====

const output =
`// Auto-generated by docs/build.js — do not edit manually
// Last updated: ${new Date().toISOString()}
const FILE_TREE = ${JSON.stringify(fileTree, null, 2)};
`;

writeFileSync(OUTPUT_FILE, output, 'utf8');

console.log(`✅  Generated docs/file-tree.js`);
console.log(`   ${allFiles.length} files / ${fileTree.length} categories`);
fileTree.forEach(cat => console.log(`   ${cat.label}: ${cat.files.length} files`));